def onesided(F):
  y = np.shape(F)[1]
  t = np.shape(F)[0]
  print(t)
  print(y)
  print(int(np.ceil(y/2))+1)
  A = np.zeros([t,int(np.ceil(y/2))])
  print(np.shape(A))
  g = y-1
  for k in range(t):
    for p in range(g):
      if(p<(g)):
        A[k][p] = F[k][p]+F[k][g]
        p += 1
        g -= 1
    g = y-1
    p = 0
  return A

